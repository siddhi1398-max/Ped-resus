// DOSE CALCULATOR UTILITY
// ─────────────────────────────────────────────────────────────────────────────
export function computeRouteDose(drug, weight) {
  if (drug.fixedDose) return { display: drug.fixedDose, raw: null };
  if (!drug.dosePerKg) return { display: "—", raw: null };

  let raw = drug.dosePerKg * weight;
  const unit = drug.doseUnit || "";

  // Unit-specific formatting
  if (unit === "mcg") {
    if (drug.max) raw = Math.min(raw, drug.max);
    return { display: `${raw.toFixed(0)} ${unit}`, raw };
  }
  if (unit === "units") {
    if (drug.max) raw = Math.min(raw, drug.max);
    return { display: `${(raw / 1000).toFixed(0)} × 1000 units (${raw.toFixed(0)} units)`, raw };
  }
  if (drug.max) raw = Math.min(raw, drug.max);
  return { display: `${raw.toFixed(1)} ${unit}`, raw };
}
