export function computeDrugDose(drug, weight) {
  if (drug.fixedDose) return drug.fixedDose;
  if (drug.dosePerKg == null) return "—";
  let d = weight * drug.dosePerKg;
  if (drug.max != null) d = Math.min(d, drug.max);
  if (drug.min != null) d = Math.max(d, drug.min);
  return `${+d.toFixed(2)} ${drug.unit}`;
}
